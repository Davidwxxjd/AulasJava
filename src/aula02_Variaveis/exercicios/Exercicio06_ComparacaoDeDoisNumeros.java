package aula02_Variaveis.exercicios;

public class Exercicio06_ComparacaoDeDoisNumeros {
    public static void main(String[] args) {

        int numero1 = 7;
        int numero2 = 4;

        System.out.println(numero1 + " > " + numero2 + ": " + (numero1 > numero2));
        System.out.println(numero1 + " < " + numero2 + ": " + (numero1 < numero2));
        System.out.println(numero1 + " >= " + numero2 + ": " + (numero1 >= numero2));
        System.out.println(numero1 + " <= " + numero2 + ": " + (numero1 <= numero2));
        System.out.println(numero1 + " == " + numero2 + ": " + (numero1 == numero2));
        System.out.println(numero1 + " != " + numero2 + ": " + (numero1 != numero2));
    }
}